package allievo;

public class Allievo {
	
	public String nome;
	public String cognome;
	public int eta;
	public String indirizzo;
	
	public Allievo(String nome, String cognome,int eta, String indirizzo){
		this.nome = nome;
		this.cognome = cognome;
		this.eta = eta;
		this.indirizzo = indirizzo;
	}
	
}
